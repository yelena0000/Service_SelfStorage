        "‚ùå*–ù–µ–ª—å–∑—è —Ö—Ä–∞–Ω–∏—Ç—å*:\n"
        "- –æ—Ä—É–∂–∏–µ, –±–æ–µ–ø—Ä–∏–ø–∞—Å—ã, –≤–∑—Ä—ã–≤—á–∞—Ç—ã–µ –≤–µ—â–µ—Å—Ç–≤–∞;\n"
        "- —Ç–æ–∫—Å–∏—á–Ω—ã–µ, —Ä–∞–¥–∏–æ–∞–∫—Ç–∏–≤–Ω—ã–µ –∏ –ª–µ–≥–∫–æ–≤–æ—Å–ø–ª–∞–º–µ–Ω—è—é—â–∏–µ—Å—è –≤–µ—â–µ—Å—Ç–≤–∞;\n"
