        "\n‚ö†Ô∏è *–ó–∞–ø—Ä–µ—â–µ–Ω–Ω—ã–µ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –≤–µ—â–∏:*\n"
        "–ú—ã –Ω–µ –ø—Ä–∏–Ω–∏–º–∞–µ–º –Ω–∞ —Ö—Ä–∞–Ω–µ–Ω–∏–µ –∏–º—É—â–µ—Å—Ç–≤–æ, –∫–æ—Ç–æ—Ä–æ–µ –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–æ –ø–æ –∑–∞–∫–æ–Ω–æ–¥–∞—Ç–µ–ª—å—Å—Ç–≤—É –†–§ "
