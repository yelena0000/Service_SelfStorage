def check_and_send_reminders(bot):
    """
        –ü—Ä–æ–≤–µ—Ä—è–µ—Ç –≤—Å–µ –∑–∞–∫–∞–∑—ã –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è–º –æ –∑–∞–≤–µ—Ä—à–µ–Ω–∏–∏ —Å—Ä–æ–∫–∞ —Ö—Ä–∞–Ω–µ–Ω–∏—è —á–µ—Ä–µ–∑ 14 –¥–Ω–µ–π.

        –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è –∏–∑–≤–ª–µ–∫–∞–µ—Ç –≤—Å–µ –∑–∞–∫–∞–∑—ã, —É –∫–æ—Ç–æ—Ä—ã—Ö –¥–∞—Ç–∞ –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è (reminder_date) —É–∂–µ –Ω–∞—Å—Ç—É–ø–∏–ª–∞,
        –∏ –≤—ã–∑—ã–≤–∞–µ—Ç —Ñ—É–Ω–∫—Ü–∏—é send_reminder –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –∏–∑ —Ç–∞–∫–∏—Ö –∑–∞–∫–∞–∑–æ–≤.
    """
    now = timezone.now()
    orders_to_remind = Order.objects.filter(reminder_date__lte=now)

    for order in orders_to_remind:
        send_reminder(bot, order.order_id)


def schedule_reminders(bot):
    """
        –ü–ª–∞–Ω–∏—Ä—É–µ—Ç –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–π –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –∫–∞–∂–¥—ã–π –¥–µ–Ω—å –≤ 09:00.

        –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è –∏—Å–ø–æ–ª—å–∑—É–µ—Ç –±–∏–±–ª–∏–æ—Ç–µ–∫—É `schedule` –¥–ª—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —Ñ—É–Ω–∫—Ü–∏–∏ `check_and_send_reminders`
        –∫–∞–∂–¥—ã–π –¥–µ–Ω—å –≤ 09:00, –ø—Ä–æ–≤–µ—Ä—è—è –∑–∞–∫–∞–∑—ã –∏ –æ—Ç–ø—Ä–∞–≤–ª—è—è –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è–º.
    """
    schedule.every().day.at("09:00").do(check_and_send_reminders, bot=bot)

    while True:
        schedule.run_pending()  # –í—ã–ø–æ–ª–Ω—è–µ–º –≤—Å–µ –∑–∞–¥–∞—á–∏, –∫–æ–≥–¥–∞ –ø—Ä–∏—Ö–æ–¥–∏—Ç –∏—Ö –≤—Ä–µ–º—è
        time.sleep(5)


