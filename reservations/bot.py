        count = size_data['count']
        price = tariffs_data.get(size, 0)
        tariffs_info += (f"- {size_labels.get(size, '–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ')} "
                         f"({count} —Å–≤–æ–±–æ–¥–Ω—ã—Ö): {price} —Ä—É–±./–¥–µ–Ω—å\n")

